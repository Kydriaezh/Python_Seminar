# Петя, Катя и Серёжа делают из бумаги журавликов. Вместе они сделали S журавликов. Сколько журавликов
# сделал каждый ребёнок, если известно, что Петя и Серёжа сделали одинаковое количество 
# журавликов, а Катя сделала в два раза больше журавликов, чем Петя и Серёжа вместе.

n = int(input('общее количество журавликов'))
